    public void updateFields(String type){
        if(type.equals("Salaried")){
            salaryCBox.setEnabled(true);
            jLabel7.setText("EMI Amount");
            savingsTxt.setEnabled(true);
        }
        else if(type.equals("Retirees")){
            salaryCBox.setEnabled(false);
            jLabel7.setText("Saving Amount");
            savingsTxt.setEnabled(true);
            
        }
        else if(type.equals("Dependents")){
            salaryCBox.setEnabled(false);
            jLabel7.setText("Savings");
            savingsTxt.setEnabled(false);
            savingsTxt.setText("0");
        }
    }

