    private void doneBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneBtnActionPerformed
        // TODO add your handling code here:
        
        try{
            int age = Integer.parseInt(ageTxt1.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please enter valid age");
        }
        double savings=0;
        try{
            savings = Double.parseDouble(savingsTxt.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please enter valid investment");
        }
        FinancialAdvisorWorkRequest request= new FinancialAdvisorWorkRequest();
        request.setNotify(true);
        request.setCategory(customerCategoryCBox1.getSelectedItem().toString());
        request.setSalary(salaryCBox.getSelectedItem().toString());
        request.setSavings(String.valueOf(savings));
        request.setGoals(selectGoalCBox.getSelectedItem().toString());
        request.setCustName(userAccount.getUsername());
        // request.setSender(account);
         //request.setFinancialAdvisor(financialAdvisor);

        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof FinancialAdvisorOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            //userAccount.getWorkQueue().getWorkRequestList().add(request);
        }

        
        
       JOptionPane.showMessageDialog(null, "Request Sent Successfully");

    }//GEN-LAST:event_doneBtnActionPerformed

